public class Contents {
    public static String id, name, image;

    public Contents(String id, String name, String image){
        this.id = id;
        this.name = name;
        this.image = image;
    }

    public String getId(){
        return this.id;
    }
    public String getName(){
        return this.id;
    }
    public String getImage(){
        return this.id;
    }

}
